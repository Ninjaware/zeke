#include <stdio.h>
#include <stdlib.h>
#include <sys/types.h>
#include <unistd.h>
#include <errno.h>
#include <time.h>
#include "tish.h"

static int tish_date(char * argv[])
{
    struct timespec ts = {0, 0};
    unsigned sec, nsec;

    if (clock_gettime(CLOCK_REALTIME, &ts))
        return -1;

    sec = ts.tv_sec;
    nsec = ts.tv_nsec;

    printf("%u.%u\n", sec, nsec);

    return 0;
}
TISH_CMD(tish_date, "date", NULL, 0);
